%--------------------------------------------------------------------------
%   初始化
%--------------------------------------------------------------------------
clear;clc;

%--------------------------------------------------------------------------
%   参数设置
%--------------------------------------------------------------------------
N = 10000;                                                                  %人口总数
I = 1;                                                                      %传染者
S = N - I;                                                                  %易感者
r = 10;                                                                     %感染者接触易感者的人数
B = 0.01;                                                                   %传染概率
y = 0.03;                                                                   %康复概率
T = 1:400;

for idx = 1:length(T)-1
    S(idx+1) = S(idx) - r*B*I(idx)*S(idx)/N + y*I(idx);
    I(idx+1) = I(idx) + r*B*I(idx)*S(idx)/N - y*I(idx);
end

plot(T,S,T,I);grid on;
xlabel('天');ylabel('人数')
legend('易感者','传染者');title('SIS模型')