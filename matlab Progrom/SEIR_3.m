%--------------------------------------------------------------------------
%   初始化
%--------------------------------------------------------------------------
clear;clc;

%--------------------------------------------------------------------------
%   参数设置
%--------------------------------------------------------------------------
N = 10000;                                                                  %人口总数
I = 1;                                                                      %传染者
S = N - I;                                                                  %易感者
R = 0;                                                                      %康复者

r = 10;                                                                     %感染者接触易感者的人数
B = 0.1;                                                                   %传染概率
y = 0.2;                                                                    %康复概率

T = 1:100;
for idx = 1:length(T)-1
    S(idx+1) = S(idx) - r*B*S(idx)*I(idx)/N;
    I(idx+1) = I(idx) + r*B*S(idx)*I(idx)/N - y*I(idx);
    R(idx+1) = R(idx) + y*I(idx);
end

plot(T,S,T,I,T,R);grid on;
xlabel('天');ylabel('人数')
legend('易感者','传染者','康复者')